// Asynchronous Javascript
// Thread : urutan eksekusi code yg dapat dilakukan secara bebas / independent satu sama lain
// Single Thread : menjalankan task secara 1 per 1 atau js membaca baris perbaris dan menyelesaikannya
// Multi Thread : menjalankan task 1 per 1 tetapi dilakukan di banyak thread.(thread = jalur, lingkup eksekusi)

// Blocking : menunggu task yg sebelumnya selesai dulu, baru task dia di eksekusi
// Non-blocking : walaupun task yg sebelumnya belum beres, maka tetap bisa lanjut ke task berikutnya

// Asynchronous : pengerjaan banyak task yg bisa dikerjakan bersama, sampai salah 1 nya selesai
// Asynchronous (Single-threaded) : pengerjaan task nya di satu thread saja
// Asynchronous (Multi-threaded) : pengerjaan task nya bisa di beberapa thread

// Asynchronous + Single-threaded = Concurrency
// Asynchronous + Multi-threaded = Parallelism

